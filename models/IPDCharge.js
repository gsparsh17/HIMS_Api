const mongoose = require('mongoose');

/**
 * IPDCharge is the operational charge source. A charge remains editable only
 * while ACTIVE and unbilled. Once invoiced it is immutable; corrections must
 * be handled through a credit note / authorised adjustment, not deletion.
 */
const ipdChargeSchema = new mongoose.Schema({
  hospitalId: { type: mongoose.Schema.Types.ObjectId, ref: 'Hospital', index: true },
  admissionId: { type: mongoose.Schema.Types.ObjectId, ref: 'IPDAdmission', required: true, index: true },
  patientId: { type: mongoose.Schema.Types.ObjectId, ref: 'Patient', required: true, index: true },
  chargeType: {
    type: String,
    enum: ['Bed', 'Doctor Visit', 'Nursing', 'Procedure', 'Lab Test', 'Pharmacy', 'Surgery', 'Equipment', 'Consultation', 'Emergency', 'Miscellaneous', 'Discount', 'Tax'],
    required: true
  },
  adjustmentType: { type: String, enum: ['CHARGE', 'DISCOUNT', 'TAX', 'WAIVER'], default: 'CHARGE' },
  status: { type: String, enum: ['ACTIVE', 'INVOICED', 'VOIDED', 'CANCELLED'], default: 'ACTIVE', index: true },
  description: { type: String, required: true, trim: true },
  quantity: { type: Number, default: 1, min: 1 },
  rate: { type: Number, default: 0, min: 0 },
  amount: { type: Number, default: 0, min: 0 },
  discount: { type: Number, default: 0, min: 0 },
  netAmount: { type: Number, default: 0 },
  tax: { type: Number, default: 0, min: 0 },
  sourceModule: {
    type: String,
    enum: ['Admission', 'Lab', 'Pharmacy', 'Procedure', 'Manual', 'Bed', 'DoctorRound', 'OT', 'Radiology', 'Billing', 'Discharge'],
    required: true
  },
  sourceId: { type: mongoose.Schema.Types.ObjectId },
  sourceReference: {
    module: { type: String, enum: ['Lab', 'Procedure', 'Radiology', 'Pharmacy', 'Bed', 'DoctorVisit', 'Manual', 'OT', 'IPD', 'Billing', 'Discharge'] },
    documentId: { type: mongoose.Schema.Types.ObjectId },
    invoiceNumber: String,
    billNumber: String,
    // Stable line identifier for legacy bills containing multiple rows that
    // originate from the same prescription or source document.
    lineKey: { type: String, trim: true, index: true }
  },
  chargeDate: { type: Date, default: Date.now },
  chargeDateKey: { type: String, index: true }, // YYYY-MM-DD for idempotent daily bed charging
  idempotencyKey: { type: String, trim: true, sparse: true },
  isAutoGenerated: { type: Boolean, default: false },
  isBilled: { type: Boolean, default: false, index: true },
  billId: { type: mongoose.Schema.Types.ObjectId, ref: 'Bill' },
  invoiceId: { type: mongoose.Schema.Types.ObjectId, ref: 'Invoice' },
  billedAt: Date,
  discountDetails: {
    type: { type: String, enum: ['fixed', 'percentage'], default: 'fixed' },
    reason: String,
    approvedBy: { type: mongoose.Schema.Types.ObjectId, ref: 'User' },
    approvedAt: Date
  },
  voidedAt: Date,
  voidedBy: { type: mongoose.Schema.Types.ObjectId, ref: 'User' },
  voidReason: String,
  addedBy: { type: mongoose.Schema.Types.ObjectId, ref: 'User' },
  notes: { type: String, trim: true },
  pricingSnapshot: {
    rateCardId: { type: mongoose.Schema.Types.ObjectId, ref: 'RateCard' },
    rateCardVersion: String,
    rateCardItemId: { type: mongoose.Schema.Types.ObjectId, ref: 'RateCardItem' },
    serviceCode: String,
    packageCode: String,
    inputs: mongoose.Schema.Types.Mixed,
    amounts: {
      hospitalStandard: { type: Number, default: 0 },
      contracted: { type: Number, default: 0 },
      sponsorLiability: { type: Number, default: 0 },
      patientLiability: { type: Number, default: 0 },
      nonAdmissible: { type: Number, default: 0 },
      hospitalAdjustment: { type: Number, default: 0 }
    },
    explanation: [String],
    ruleTrace: [mongoose.Schema.Types.Mixed],
    pricedAt: Date
  },
  patientLiability: { type: Number, default: 0, min: 0 },
  sponsorLiability: { type: Number, default: 0, min: 0 },
  nonAdmissibleAmount: { type: Number, default: 0, min: 0 },
  rateCardId: { type: mongoose.Schema.Types.ObjectId, ref: 'RateCard' },
  rateCardVersion: String,
  packageCode: String,
  accommodationSegmentId: { type: mongoose.Schema.Types.ObjectId, ref: 'IPDAccommodationSegment' },
}, { timestamps: true });

ipdChargeSchema.pre('validate', function calculateCharge(next) {
  this.amount = Number(this.quantity || 0) * Number(this.rate || 0);
  this.netAmount = Number((this.amount - Number(this.discount || 0) + Number(this.tax || 0)).toFixed(2));
  if (!this.chargeDateKey && this.chargeDate) {
    this.chargeDateKey = new Date(this.chargeDate).toISOString().slice(0, 10);
  }
  if (!this.pricingSnapshot || !this.pricingSnapshot.amounts) {
    this.pricingSnapshot = {
      serviceCode: this.sourceReference?.lineKey,
      inputs: { payer: 'SELF_OR_UNPRICED', serviceDate: this.chargeDate },
      amounts: {
        hospitalStandard: Number(this.amount || 0),
        contracted: Number(this.netAmount || 0),
        sponsorLiability: 0,
        patientLiability: Number(this.netAmount || 0),
        nonAdmissible: 0,
        hospitalAdjustment: 0
      },
      explanation: ['Standard hospital pricing snapshot'],
      pricedAt: new Date()
    };
  }
  this.patientLiability = Number(this.pricingSnapshot?.amounts?.patientLiability ?? this.netAmount ?? 0);
  this.sponsorLiability = Number(this.pricingSnapshot?.amounts?.sponsorLiability ?? 0);
  this.nonAdmissibleAmount = Number(this.pricingSnapshot?.amounts?.nonAdmissible ?? 0);
  this.rateCardId = this.pricingSnapshot?.rateCardId || this.rateCardId;
  this.rateCardVersion = this.pricingSnapshot?.rateCardVersion || this.rateCardVersion;
  this.packageCode = this.pricingSnapshot?.packageCode || this.packageCode;

  if (this.adjustmentType === 'DISCOUNT' || this.chargeType === 'Discount') {
    this.rate = 0;
    this.amount = 0;
    this.netAmount = Number((-Math.abs(Number(this.discount || 0))).toFixed(2));
  }
  next();
});

ipdChargeSchema.index({ admissionId: 1, chargeType: 1, status: 1 });
ipdChargeSchema.index({ patientId: 1, chargeDate: -1 });
ipdChargeSchema.index({ sourceModule: 1, sourceId: 1 });
ipdChargeSchema.index({ admissionId: 1, isBilled: 1, status: 1 });
ipdChargeSchema.index({ admissionId: 1, chargeDateKey: 1, chargeType: 1, status: 1 });
ipdChargeSchema.index({ idempotencyKey: 1 }, { unique: true, sparse: true });

module.exports = mongoose.model('IPDCharge', ipdChargeSchema);
