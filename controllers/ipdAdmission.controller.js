function activeAdmissionFilter2026() {
  return {
    $in: [
      'Admitted',
      'Under Treatment',
      'Discharge Initiated',
      'Discharge Summary Pending',
      'Billing Pending',
      'Payment Pending',
      'Ready for Discharge'
    ]
  };
}

function normalizedPricingSnapshot2026(quote) {
  return {
    rateCardId: quote.rateCard?.id,
    rateCardVersion: quote.rateCard?.version,
    rateCardItemId: quote.rateCardItemId,
    serviceCode: quote.serviceCode,
    packageCode: quote.packageCode,
    inputs: quote.inputs,
    amounts: quote.amounts,
    explanation: quote.explanation,
    ruleTrace: quote.ruleTrace,
    pricedAt: new Date()
  };
}

async function quoteWithStandardFallback2026(input) {
  try {
    return await quoteAdmissionPricing2026(input);
  } catch (error) {
    const total = Number(input.standardAmount ?? input.rate ?? 0) * Number(input.quantity || 1);
    return {
      serviceCode: input.externalCode || null,
      rateCard: null,
      inputs: {
        payer: 'PENDING_MAPPING',
        serviceDate: input.serviceDate || new Date(),
        fallbackReason: error.message
      },
      amounts: {
        hospitalStandard: total,
        contracted: total,
        patientLiability: total,
        sponsorLiability: 0,
        nonAdmissible: 0,
        hospitalAdjustment: 0
      },
      explanation: ['Standard hospital amount used while payer mapping is pending'],
      ruleTrace: []
    };
  }
}

async function ensureSelfPayer2026(hospitalId, userId, session) {
  let payer = await Payer2026.findOne({ hospitalId, code: 'SELF' }).session(session || null);

  if (!payer) {
    [payer] = await Payer2026.create([{
      hospitalId,
      code: 'SELF',
      name: 'Self Pay',
      type: 'self',
      empanelment: { status: 'not_required' },
      isActive: true,
      createdBy: userId,
      updatedBy: userId
    }], { session });
  }

  return payer;
}

async function createInitialCharge2026({
  hospitalId,
  admission,
  chargeType,
  description,
  rate,
  sourceModule,
  sourceId,
  addedBy,
  coverage,
  serviceType,
  internalServiceModel,
  internalServiceId,
  externalCode,
  session,
  isBilled = false,
  invoiceId
}) {
  const quote = await quoteWithStandardFallback2026({
    hospitalId,
    admissionId: admission._id,
    coverage,
    serviceDate: new Date(),
    chargeType,
    serviceType,
    internalServiceModel,
    internalServiceId,
    externalCode,
    standardAmount: Number(rate || 0),
    quantity: 1
  });

  const [charge] = await IPDCharge.create([{
    hospitalId,
    admissionId: admission._id,
    patientId: admission.patientId,
    chargeType,
    description,
    quantity: 1,
    rate: Number(quote.amounts?.contracted ?? rate ?? 0),
    amount: Number(quote.amounts?.contracted ?? rate ?? 0),
    netAmount: Number(quote.amounts?.contracted ?? rate ?? 0),
    sourceModule,
    sourceId,
    isAutoGenerated: true,
    isBilled,
    invoiceId,
    addedBy,
    chargeDate: new Date(),
    chargeDateKey: new Date().toISOString().slice(0, 10),
    pricingSnapshot: normalizedPricingSnapshot2026(quote),
    patientLiability: Number(quote.amounts?.patientLiability || 0),
    sponsorLiability: Number(quote.amounts?.sponsorLiability || 0),
    nonAdmissibleAmount: Number(quote.amounts?.nonAdmissible || 0)
  }], { session });

  return charge;
}

// ========== HELPER: Generate Invoice for Registration Fee ==========
async function generateRegistrationFeeInvoice(admission, patient, registrationFee, paymentMethod, createdBy) {
  try {
    if (!registrationFee || registrationFee <= 0) return null;

    // Create a bill for the registration fee
    const bill = new Bill({
      hospital_id: admission.hospitalId,
      patient_id: patient._id,
      admission_id: admission._id,
      total_amount: registrationFee,
      subtotal: registrationFee,
      tax_amount: 0,
      discount: 0,
      payment_method: paymentMethod || 'Cash',
      status: 'Paid',
      paid_amount: registrationFee,
      balance_due: 0,
      paid_at: new Date(),
      items: [{
        description: `IPD Registration Fee - ${admission.admissionNumber}`,
        amount: registrationFee,
        quantity: 1,
        item_type: 'Other',
        admission_id: admission._id
      }],
      notes: `Registration fee for IPD admission ${admission.admissionNumber}`,
      created_by: createdBy,
      is_pharmacy_bill: false
    });

    await bill.save();

    // Create invoice for the registration fee
    const invoice = new Invoice({
      hospital_id: admission.hospitalId,
      invoice_type: 'IPD Registration',
      patient_id: patient._id,
      admission_id: admission._id,
      customer_type: 'Patient',
      customer_name: `${patient.first_name} ${patient.last_name || ''}`.trim(),
      customer_phone: patient.phone,
      bill_id: bill._id,
      issue_date: new Date(),
      due_date: new Date(),
      service_items: [{
        description: `IPD Registration Fee - ${admission.admissionNumber}`,
        quantity: 1,
        unit_price: registrationFee,
        total_price: registrationFee,
        tax_rate: 0,
        tax_amount: 0,
        service_type: 'Other'
      }],
      subtotal: registrationFee,
      discount: 0,
      tax: 0,
      total: registrationFee,
      amount_paid: registrationFee,
      balance_due: 0,
      status: 'Paid',
      notes: `Registration fee for IPD admission ${admission.admissionNumber}`,
      created_by: createdBy,
      payment_history: [{
        amount: registrationFee,
        method: paymentMethod || 'Cash',
        date: new Date(),
        status: 'Completed',
        collected_by: createdBy
      }]
    });

    await invoice.save();
    bill.invoice_id = invoice._id;
    await bill.save();

    return { bill, invoice };
  } catch (error) {
    console.error('Error generating registration fee invoice:', error);
    return null;
  }
}

