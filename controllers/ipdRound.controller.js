const IPDRound = require('../models/IPDRound');
const IPDAdmission = require('../models/IPDAdmission');
const IPDCharge = require('../models/IPDCharge');

// Create round entry
// Create round entry with medication prescriptions
exports.createRound = async (req, res) => {
  try {
    const {
      admissionId,
      patientId,
      doctorId,
      roundDateTime,
      patientCondition,
      complaints,
      symptoms,
      examinationFindings,
      diagnosis,
      treatmentPlan,
      advice,
      medicationsPrescribed, // Array of medications from doctor
      investigationsOrdered,
      proceduresOrdered,
      dischargeSuggested,
      nextReviewDate,
      notes
    } = req.body;
    
    // Verify admission exists
    const admission = await IPDAdmission.findById(admissionId);
    if (!admission) {
      return res.status(404).json({ error: 'Admission not found' });
    }
    
    // Create round
    const round = new IPDRound({
      admissionId,
      patientId,
      doctorId,
      roundDateTime: roundDateTime || new Date(),
      patientCondition,
      complaints,
      symptoms,
      examinationFindings,
      diagnosis,
      treatmentPlan,
      advice,
      medicationsPrescribed,
      investigationsOrdered,
      proceduresOrdered,
      dischargeSuggested,
      nextReviewDate,
      notes,
      createdBy: req.user?._id
    });
    
    await round.save();
    
    // Create medication orders from prescribed medications
    const IPDMedicationChart = require('../models/IPDMedicationChart');
    const createdMedications = [];
    
    if (medicationsPrescribed && medicationsPrescribed.length > 0) {
      for (const med of medicationsPrescribed) {
        const medicationOrder = new IPDMedicationChart({
          admissionId,
          patientId,
          prescribedBy: doctorId,
          roundId: round._id,
          medicineName: med.medicineName,
          dosage: med.dosage,
          frequency: med.frequency,
          duration: med.duration,
          route: med.route || 'Oral',
          startDate: new Date(),
          requiresPharmacyDispense: med.requiresPharmacyDispense || false,
          createdBy: req.user?._id
        });
        
        await medicationOrder.save();
        createdMedications.push(medicationOrder);
        
        // Create nursing note for new medication
        const NursingNote = require('../models/NursingNote');
        const nursingNote = new NursingNote({
          admissionId,
          patientId,
          noteType: 'Medication',
          note: `New medication prescribed: ${med.medicineName} ${med.dosage} ${med.frequency}`,
          priority: med.isHighRisk ? 'Important' : 'Normal',
          createdBy: req.user?._id
        });
        await nursingNote.save();
      }
    }
    
    // Update admission status if discharge suggested
    if (dischargeSuggested && admission.canProceedToDischarge) {
      admission.status = 'Discharge Initiated';
      await admission.save();
    }
    
    // Add doctor visit charge
    const IPDCharge = require('../models/IPDCharge');
    const doctorVisitCharge = new IPDCharge({
      admissionId,
      patientId,
      chargeType: 'Doctor Visit',
      description: `Doctor round by Dr. ${req.user?.name || 'Staff'} on ${new Date().toLocaleDateString()}`,
      quantity: 1,
      rate: 100,
      amount: 100,
      sourceModule: 'DoctorRound',
      sourceId: round._id,
      isAutoGenerated: true,
      addedBy: req.user?._id
    });
    await doctorVisitCharge.save();
    
    res.status(201).json({
      success: true,
      message: 'Round note added successfully',
      round,
      medications: createdMedications,
      charge: doctorVisitCharge
    });
  } catch (err) {
    console.error('Error creating round:', err);
    res.status(500).json({ error: err.message });
  }
};

// Get rounds by admission
exports.getRoundsByAdmission = async (req, res) => {
  try {
    const { admissionId } = req.params;
    
    const rounds = await IPDRound.find({ admissionId })
      .populate('doctorId', 'firstName lastName specialization')
      .sort({ roundDateTime: -1 });
    
    res.json({ success: true, rounds });
  } catch (err) {
    console.error('Error fetching rounds:', err);
    res.status(500).json({ error: err.message });
  }
};

// Get round by ID
exports.getRoundById = async (req, res) => {
  try {
    const { id } = req.params;
    
    const round = await IPDRound.findById(id)
      .populate('doctorId', 'firstName lastName specialization')
      .populate('admissionId', 'admissionNumber patientId');
    
    if (!round) {
      return res.status(404).json({ error: 'Round not found' });
    }
    
    res.json({ success: true, round });
  } catch (err) {
    console.error('Error fetching round:', err);
    res.status(500).json({ error: err.message });
  }
};

// Update round
exports.updateRound = async (req, res) => {
  try {
    const { id } = req.params;
    const updates = req.body;
    
    const round = await IPDRound.findByIdAndUpdate(id, updates, { new: true });
    if (!round) {
      return res.status(404).json({ error: 'Round not found' });
    }
    
    res.json({
      success: true,
      message: 'Round updated successfully',
      round
    });
  } catch (err) {
    console.error('Error updating round:', err);
    res.status(500).json({ error: err.message });
  }
};

// Delete round
exports.deleteRound = async (req, res) => {
  try {
    const { id } = req.params;
    
    const round = await IPDRound.findByIdAndDelete(id);
    if (!round) {
      return res.status(404).json({ error: 'Round not found' });
    }
    
    res.json({
      success: true,
      message: 'Round deleted successfully'
    });
  } catch (err) {
    console.error('Error deleting round:', err);
    res.status(500).json({ error: err.message });
  }
};

// Get rounds by doctor
exports.getRoundsByDoctor = async (req, res) => {
  try {
    const { doctorId } = req.params;
    const { startDate, endDate } = req.query;
    
    const filter = { doctorId };
    if (startDate && endDate) {
      filter.roundDateTime = {
        $gte: new Date(startDate),
        $lte: new Date(endDate)
      };
    }
    
    const rounds = await IPDRound.find(filter)
      .populate('admissionId', 'admissionNumber patientId')
      .populate('patientId', 'first_name last_name patientId')
      .sort({ roundDateTime: -1 });
    
    res.json({ success: true, rounds });
  } catch (err) {
    console.error('Error fetching rounds by doctor:', err);
    res.status(500).json({ error: err.message });
  }
};